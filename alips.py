# ELIF : Else If Statement

nama = input("Siapa nama kamu? ")

if nama == "iori": #kondisi 1
    print("Hi si tampan dan berani") #aksi true 1
elif nama == "chel": #kondisi 2
    print("Oh hi masa lalu!!!") #aksi true 2
elif nama == "viana": #kondisi 3
    print("Oh hi cheaterr!!!") #aksi true 3
else: #kondisi 4
    print("siapa luh_- ???") #aksi false

